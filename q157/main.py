class Solution:
    def read(self, buf, n):
        """
        :type buf: Destination buffer (List[str])
        :type n: Number of characters to read (int)
        :rtype: The number of actual characters read (int)
        """
        buf4 = [''] * 4
        read = 0
        while read < n:
            count = read4(buf4)
            if not count: break
            count = min(count, n - read)
            buf[read:] = buf4[:count]
            read += count
        return read     

